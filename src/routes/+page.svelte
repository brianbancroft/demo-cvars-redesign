<section>
	<h2>CVARS</h2>

	<p>
		Cowichan Valley Amateur Radio Society (CVARS) is an incorporated Not For Profit group. CVARS is
		comprised of amateur radio operators from Ladysmith to the Malahat including Lake Cowichan and
		the Gulf Islands. The Society maintains a number of voice repeaters and digipeaters located near
		Duncan on Vancouver Island, British Columbia, Canada.
	</p>
</section>

